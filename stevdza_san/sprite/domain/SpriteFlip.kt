package com.stevdza_san.sprite.domain

/**
 * Enum class representing the possible flip directions for a sprite.
 *
 * - `Horizontal`: Flips the sprite horizontally (left-to-right).
 * - `Vertical`: Flips the sprite vertically (top-to-bottom).
 * - `Both`: Flips the sprite both horizontally and vertically.
 */
enum class SpriteFlip {
    Horizontal,
    Vertical,
    Both
}